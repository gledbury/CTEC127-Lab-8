      <h3>Here are the formulas used to convert the temperatures:</h3>
      <br>
      <h4>Celsius to Fahrenheit Conversion:</h4>
      <p>The temperature T in degrees Fahrenheit (°F) is equal to the temperature T in degrees Celsius (°C) times 9/5 plus 32:</p>
      <p>T(°F) = T(°C) × 9/5 + 32</p>
      <br>
      <h4>Celsius to Kelvin Conversion:</h4>
      <p>The temperature T in Kelvin (K) is equal to the temperature T in degrees Celsius (°C) plus 273.15:</p>
      <p>T(K) = T(°C) + 273.15</p>
      <br>
      <h4>Fahrenheit to Celsius Conversion:</h4>
      <p>The temperature T in degrees Celsius (°C) is equal to the temperature T in degrees Fahrenheit (°F) minus 32, times 5/9:</p>
      <p>T(°C) = (T(°F) - 32) × 5/9</p>
      <br>
      <h4>Fahrenheit to Kelvin Conversion:</h4>
      <p>The temperature T in Kelvin (K) is equal to the temperature T in degrees Fahrenheit (°F) plus 459.67, times 5/9:</p>
      <p>T(K) = (T(°F) + 459.67) × 5/9</p>
      <br>
      <h4>Kelvin to Celsius Conversion:</h4>
      <p>The temperature T in degrees Celsius (°C) is equal to the temperature T in Kelvin (K) minus 273.15:</p>
      <p>T(°C) = T(K) - 273.15</p>
      <br>
      <h4>Kelvin to Fahrenheit Conversion:</h4>
      <p>The temperature T in degrees Fahrenheit (°F) is equal to the temperature T in Kelvin (K) times 9/5, minus 459.67:</p>
      <p>T(°F) = T(K) × 9/5 - 459.67</p>
