        <h3>Definitions of the three temperature scales used in this program.</h3>
        <br>
        <h4>Celsius</h4>
        <p>The Celsius scale is also known as the centigrade scale. It's symbol, °C, can refer to a specific temperature on the Celsius scale or a unit to indicate a difference between two temperatures or an uncertainty. It is named after the Swedish astronomer Anders Celsius (1701–1744), who developed a similar temperature scale. Before being renamed to honor Anders Celsius in 1948, the unit was called centigrade, from the Latin centum, which means 100, and gradus, which means steps.</p>
        <br>
        <h4>Fahrenheit</h4>
        <p>The Fahrenheit scale is a temperature scale that uses the degree Fahrenheit (symbol: °F) as the unit.  On the Fahrenheit scale, the freezing point of water is 32 degrees Fahrenheit (°F) and the boiling point is 212 °F (at standard atmospheric pressure). This puts the boiling and freezing points of water 180 degrees apart.[5] Therefore, a degree on the Fahrenheit scale is ​1⁄180 of the interval between the freezing point and the boiling point. On the Celsius scale, the freezing and boiling points of water are 100 degrees apart. A temperature interval of 1 °F is equal to an interval of ​5⁄9 degrees Celsius. The Fahrenheit and Celsius scales intersect at −40° (i.e., −40 °F = −40 °C).</p>
        <br>
        <h4>Kelvin</h4>
        <p>The kelvin is the base unit of temperature in the International System of Units (SI), having the unit symbol K.  Unlike the degree Fahrenheit and degree Celsius, the kelvin is not referred to or written as a degree. The kelvin is the primary unit of temperature measurement in the physical sciences, but is often used in conjunction with the degree Celsius, which has the same magnitude.  The kelvin is often used as a measure of the colour temperature of light sources. Colour temperature is based upon the principle that a black body radiator emits light with a frequency distribution characteristic of its temperature. Black bodies at temperatures below about 4000 K appear reddish, whereas those above about 7500 K appear bluish. Colour temperature is important in the fields of image projection and photography, where a colour temperature of approximately 5600 K is required to match "daylight" film emulsions. In astronomy, the stellar classification of stars and their place on the Hertzsprung–Russell diagram are based, in part, upon their surface temperature, known as effective temperature. The photosphere of the Sun, for instance, has an effective temperature of 5778 K.</p>
